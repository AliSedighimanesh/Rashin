function brand(id, nameFa, nameEn, descFa, descEn) {
  return { id, nameFa, nameEn, descFa, descEn };
}

export function getCategory(productItem) {
  return categories.find((categoryItem) => categoryItem.id === productItem.categoryId);
}

export function localizedProduct(productItem, lang) {
  const categoryItem = getCategory(productItem);
  return {
    ...productItem,
    name: lang === 'fa' ? productItem.productNameFa : productItem.productNameEn,
    unit: lang === 'fa' ? productItem.unitFa : productItem.unitEn,
    shortDescription: lang === 'fa' ? productItem.shortDescriptionFa : productItem.shortDescriptionEn,
    fullDescription: lang === 'fa' ? productItem.fullDescriptionFa : productItem.fullDescriptionEn,
    categoryName: lang === 'fa' ? categoryItem?.categoryNameFa : categoryItem?.categoryNameEn
  };
}

export function localizedCategory(categoryItem, lang) {
  return {
    ...categoryItem,
    name: lang === 'fa' ? categoryItem.categoryNameFa : categoryItem.categoryNameEn,
    description: lang === 'fa' ? categoryItem.descriptionFa : categoryItem.descriptionEn,
    productCount: products.filter((productItem) => productItem.categoryId === categoryItem.id).length
  };
}

export function localizedBrand(brandItem, lang) {
  return {
    ...brandItem,
    name: lang === 'fa' ? brandItem.nameFa : brandItem.nameEn,
    description: lang === 'fa' ? brandItem.descFa : brandItem.descEn,
    productCount: products.filter((productItem) => productItem.brandId === brandItem.id).length || 4
  };
}
